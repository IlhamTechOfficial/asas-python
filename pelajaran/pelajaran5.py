def tolak(nombor, nombor2):
    return nombor - nombor2


def tambah(nombor, nombor2):
    return nombor + nombor2


def bahagi(nombor, nombor2):
    return nombor / nombor2


def darab(nombor, nombor2):
    return nombor * nombor2


if __name__ == '__main__':
    print(tolak(21, 5))
    print(tambah(21, 5))
    print(bahagi(21, 5))
    print(darab(21, 5))